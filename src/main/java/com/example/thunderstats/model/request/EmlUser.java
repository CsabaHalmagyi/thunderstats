package com.example.thunderstats.model.request;

public class EmlUser {

    private String emlDir;
    private String email;

    public EmlUser() {
    }

    public String getEmlDir() {
        return emlDir;
    }

    public void setEmlDir(String emlDir) {
        this.emlDir = emlDir;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
